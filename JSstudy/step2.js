var num = 3; 

switch (num) {
    case 1:
        console.log("num 변수의 값은 1입니다.");
        break;

    case 2:
        console.log("num 변수의 값은 2입니다.");
        break;

    case 3:
        console.log("num 변수의 값은 3입니다.");
        break;

    case 4:
        console.log("num 변수의 값은 4입니다.");
        break;

    case 5:
        console.log("num 변수의 값은 5입니다.");
        break;

    default:
        console.log("잘못된 입력입니다.");
        break;
}

